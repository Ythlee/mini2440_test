@	功能 :  LED点灯程序(由汇编转入C程序)
@******************************************************************************

.text
.global _start
_start:
			ldr			r0,=0x53000000		@关闭看门狗

			mov			r1,#0x0				@r1设置为0x00000000
			str			r1,[r0]				@将r1的值写到r0中所存的地址中

			ldr			sp,=1024*4			@设置栈大小

			bl			main				@进入C中的main函数
main_loop:
			b			main_loop			@死循环
