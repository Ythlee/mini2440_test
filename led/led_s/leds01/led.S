@******************************************************************************
@	File :  led.S
@	功能 :  LED点灯程序，点亮1个LED
@******************************************************************************

.text
.global _start
_start:
			ldr			r0,=0x56000010		@r0设为GPBCON寄存器

			mov			r1,#0x00000400		@r1设置为0x00000400
			str			r1,[r0]				@将r1的值写到r0中所存的地址中

			ldr			r0,=0x56000014		@设置为GPBDAT寄存器
			mov			r1,#0x00000000		@将r1赋值为0
			str			r1,[r0]				@将r1的值写到r0中所存的地址中

main_loop:
			b			main_loop			@死循环
